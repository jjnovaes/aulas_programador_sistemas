package aula;

public class ExemploFor {
	public static void main(String args[]) {
		//for -> estrura de repetição
		//vai ter sempre variável de controle
		//vai ter um valor de inicio e valor final
		//vai ter um incremento
		
		System.out.println("Inicio do programa");
	
		for(int i = 1; i <= 20; i++) {
			System.out.println("Estou repetindo pela " + i);
		}
		
		System.out.println("Fim do programa");
	}
}

package classes;

public class ExemploVariavel {

	public static void main(String[] args) {	
		Integer idade;
		Double salario;
		Character sexo;
		Boolean situacao;
		String nome; //String armazena um cadeia de caracteres
		
		idade = 18;
		salario = 1200.99;
		sexo = 'M';
		situacao = true;
		nome = "Joao";
		
		System.out.println("Idade: " + idade);
		System.out.println("Salario: " + salario);
		System.out.println("Sexo: " + sexo);
		System.out.println("Sit: " + situacao);
		System.out.println("Nome: " + nome);

	}

}
